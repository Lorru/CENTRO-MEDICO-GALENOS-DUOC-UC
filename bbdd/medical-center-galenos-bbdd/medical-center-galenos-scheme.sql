CREATE OR REPLACE
PROCEDURE EXCEPTION_LOG_SP
(
exceptionLogDescription EXCEPTION_LOG.EXCEPTION_LOG_DESCRIPTION%TYPE, 
exceptionLogDate EXCEPTION_LOG.EXCEPTION_LOG_DATE%TYPE, 
exceptionLogStatusCode EXCEPTION_LOG.EXCEPTION_LOG_STATUS_CODE%TYPE, 
exceptionLogController EXCEPTION_LOG.EXCEPTION_LOG_CONTROLLER%TYPE, 
exceptionLogMethod EXCEPTION_LOG.EXCEPTION_LOG_METHOD%TYPE) 
IS

BEGIN
INSERT INTO EXCEPTION_LOG (EXCEPTION_LOG_ID, EXCEPTION_LOG_DESCRIPTION, EXCEPTION_LOG_DATE, EXCEPTION_LOG_STATUS_CODE, EXCEPTION_LOG_CONTROLLER, EXCEPTION_LOG_METHOD) 
VALUES (EXCEPTION_LOG_SEQ.NEXTVAL, exceptionLogDescription, exceptionLogDate, exceptionLogStatusCode, exceptionLogController, exceptionLogMethod);
END;

CREATE OR REPLACE
PROCEDURE EVENT_LOG_SP
(
userId EVENT_LOG.USER_ID%TYPE, 
eventLogDescription EVENT_LOG.EVENT_LOG_DESCRIPTION%TYPE, 
eventLogDate EVENT_LOG.EVENT_LOG_DATE%TYPE, 
eventLogStatusCode EVENT_LOG.EVENT_LOG_STATUS_CODE%TYPE) 
IS

BEGIN
INSERT INTO EVENT_LOG (EVENT_LOG_ID, USER_ID, EVENT_LOG_DESCRIPTION, EVENT_LOG_DATE, EVENT_LOG_STATUS_CODE) 
VALUES (EVENT_LOG_SEQ.NEXTVAL, userId, eventLogDescription, eventLogDate, eventLogStatusCode);
END;

